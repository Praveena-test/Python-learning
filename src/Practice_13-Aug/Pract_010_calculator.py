# take 2 number input from from user and perform add, div, mul & sub

num1 = int(input("Enter first number:"))
num2 = int(input("Enter second number:"))

print("Sum", num1+num2)
print("Sub", num1-num2)
print("mul", num1*num2)
print("div", num1/num2)
# division --> Python automatically changes the datatype to decimal/float even though
