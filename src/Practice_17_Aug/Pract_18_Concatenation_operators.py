#Concatenation
# When the + is used between two numbers it performs arithmetic operation
# When + is used between string it will concate the strings
# However + can't be used to concatenate different types i.e number and string can't be appended
print(2+2)
print(True+5) # python considers true as 1
print(True+True)
print((False*9)) # python considers false as 0
print('Welcome'+'Praveena') #WelcomePraveena

